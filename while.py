    print(f"counter values set with loop with while is {counter} and the length of the list {len(items)} used as check")
    print(f"a while loop counter becomes an infinite loop if not stopping criteria is set")
    
    print(f"items[counter] is {items[counter]}")
    counter= counter+1 
    
